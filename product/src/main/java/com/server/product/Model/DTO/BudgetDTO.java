package com.server.product.Model.DTO;

public class BudgetDTO {
    private String id;  // Budget ID
    private String userId;  // User who owns this budget
    private String category;  // Budget category
    private double amount;  // Total budget amount
    private String period;  // Monthly, yearly, etc.

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }
}
